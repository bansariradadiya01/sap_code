//find the element in sorted matrix
public class matrix02 {
    public static void main(String[] args) {
        int matrix[][]={{1,2,3},{4,5,6},{7,8,9}};
        int v=5;
        for(int i=0;i<matrix.length;i++){
            for(int j=0;j<matrix[0].length;j++){
                if(matrix[i][j]==v){
                    System.out.println(v);

                }
                break;


    }}}
}
